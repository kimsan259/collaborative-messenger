package com.messenger.chat.entity;

/**
 * ============================================================
 * RoomType - 채팅방 유형
 * ============================================================
 *
 * 【종류】
 * - DIRECT: 1:1 대화방 (두 사람만 참여)
 * - GROUP:  그룹 대화방 (3명 이상 참여 가능)
 * ============================================================
 */
public enum RoomType {
    DIRECT,     // 1:1 채팅방
    GROUP       // 그룹 채팅방
}
