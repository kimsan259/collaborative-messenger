package com.messenger.chat.entity;

/**
 * ============================================================
 * MessageType - 채팅 메시지 유형
 * ============================================================
 */
public enum MessageType {
    TEXT,       // 일반 텍스트 메시지
    IMAGE,      // 이미지 (향후 확장)
    FILE,       // 파일 첨부 (향후 확장)
    SYSTEM      // 시스템 메시지 (예: "김개발님이 입장했습니다")
}
