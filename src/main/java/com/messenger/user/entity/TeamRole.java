package com.messenger.user.entity;

/**
 * ============================================================
 * TeamRole - 팀 내에서의 역할
 * ============================================================
 *
 * 【역할】
 * 특정 팀 안에서 사용자가 어떤 역할인지를 나타냅니다.
 * UserRole(시스템 전체 권한)과는 다르게, 팀 단위의 역할입니다.
 *
 * 예시: 김개발은 시스템 전체에서는 MEMBER이지만, 개발팀에서는 LEADER
 * ============================================================
 */
public enum TeamRole {
    LEADER,     // 팀장
    MEMBER      // 팀원
}
