package com.messenger.user.entity;

/**
 * ============================================================
 * FriendshipStatus - 친구 관계 상태
 * ============================================================
 *
 * 【상태 흐름】
 * 1. A가 B에게 친구 요청 → PENDING
 * 2-a. B가 수락 → ACCEPTED (친구 관계 성립)
 * 2-b. B가 거절 → REJECTED
 * 3. 어느 쪽이든 차단 가능 → BLOCKED
 * ============================================================
 */
public enum FriendshipStatus {
    /** 친구 요청 대기 중 (수신자가 아직 응답하지 않음) */
    PENDING,

    /** 친구 요청 수락됨 (서로 친구) */
    ACCEPTED,

    /** 친구 요청 거절됨 */
    REJECTED,

    /** 차단됨 */
    BLOCKED
}
