package com.messenger.user.entity;

/**
 * ============================================================
 * UserRole - 사용자 권한 등급
 * ============================================================
 *
 * 【역할】
 * 시스템 전체에서의 사용자 권한을 나타냅니다.
 * Spring Security에서 접근 제어에 사용됩니다.
 *
 * 【권한별 기능】
 * - ADMIN:   시스템 관리 (사용자 관리, 팀 관리, 모든 기능 접근)
 * - MANAGER: 팀 관리 + 업무일지 조회 (팀원의 업무일지를 볼 수 있음)
 * - MEMBER:  일반 사용자 (채팅, 본인 업무일지 조회)
 * ============================================================
 */
public enum UserRole {
    ADMIN,      // 관리자
    MANAGER,    // 매니저 (팀장급)
    MEMBER      // 일반 사용자
}
