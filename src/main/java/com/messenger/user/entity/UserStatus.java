package com.messenger.user.entity;

/**
 * ============================================================
 * UserStatus - 사용자 접속 상태
 * ============================================================
 *
 * 【역할】
 * 사용자의 현재 접속 상태를 나타냅니다.
 * 채팅방 멤버 목록에서 "온라인/오프라인" 표시에 사용됩니다.
 *
 * 【상태 변경 시점】
 * - ONLINE:  WebSocket 연결 성공 시
 * - OFFLINE: WebSocket 연결 해제 시
 * - AWAY:    5분간 활동 없을 시 (향후 구현 예정)
 * ============================================================
 */
public enum UserStatus {
    ONLINE,     // 접속 중
    OFFLINE,    // 오프라인
    AWAY        // 자리 비움
}
