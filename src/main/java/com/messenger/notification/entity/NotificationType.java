package com.messenger.notification.entity;

/**
 * ============================================================
 * NotificationType - 알림 유형
 * ============================================================
 *
 * 【종류】
 * - MENTION: 채팅에서 @멘션 당했을 때
 * - REPORT:  업무일지가 자동 생성되었을 때
 * - SYSTEM:  시스템 공지사항
 * ============================================================
 */
public enum NotificationType {
    /** 채팅 멘션 알림 - "@홍길동" 형태로 언급되었을 때 */
    MENTION,

    /** 업무일지 생성 알림 - 매일 18:00에 자동 생성 시 */
    REPORT,

    /** 친구 요청 알림 - 다른 사용자가 친구 요청을 보냈을 때 */
    FRIEND_REQUEST,

    /** 친구 수락 알림 - 상대방이 친구 요청을 수락했을 때 */
    FRIEND_ACCEPTED,

    /** 시스템 알림 - 관리자가 보내는 공지 등 */
    SYSTEM
}
