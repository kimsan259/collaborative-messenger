package com.messenger.report.entity;

/**
 * ============================================================
 * WorkCategory - 업무 카테고리
 * ============================================================
 *
 * 【역할】
 * 업무일지 항목(ReportItem)의 업무 종류를 분류합니다.
 * MessageAnalyzer가 채팅 메시지의 키워드를 분석하여 자동으로 분류합니다.
 *
 * 【분류 키워드 예시】
 * - DEVELOPMENT: "코드", "버그", "수정", "배포", "개발", "구현", "PR", "커밋"
 * - MEETING:     "회의", "미팅", "논의", "결정", "안건", "일정"
 * - REVIEW:      "검토", "확인", "피드백", "리뷰", "승인"
 * - OTHER:       위 키워드에 해당하지 않는 경우
 * ============================================================
 */
public enum WorkCategory {
    DEVELOPMENT,    // 개발 업무
    MEETING,        // 회의/미팅
    REVIEW,         // 코드 리뷰/문서 검토
    OTHER           // 기타
}
