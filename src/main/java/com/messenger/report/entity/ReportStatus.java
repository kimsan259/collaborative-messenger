package com.messenger.report.entity;

/**
 * ============================================================
 * ReportStatus - 업무일지 상태
 * ============================================================
 *
 * 【상태 흐름】
 * AUTO_GENERATED → (사용자가 수정) → EDITED → (사용자가 확인) → CONFIRMED
 * ============================================================
 */
public enum ReportStatus {
    AUTO_GENERATED,   // 시스템이 자동 생성한 상태 (아직 사용자 확인 안 됨)
    EDITED,           // 사용자가 내용을 수정한 상태
    CONFIRMED         // 사용자가 확인 완료한 상태
}
